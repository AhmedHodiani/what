/**
 * EmojiPropertiesPanel - Emoji picker with categories
 *
 * Features:
 * - 6 emoji categories
 * - Grid layout with selectable emoji
 * - Teal theme colors
 */
export function EmojiPropertiesPanel({
  object,
  onUpdate,
}: EmojiPropertiesPanelProps) {
  const currentEmoji = object.object_data.emoji

  const handleEmojiSelect = (emoji: string) => {
    onUpdate(object.id, {
      object_data: {
        ...object.object_data,
        emoji,
      },
    })
  }

  return (
    <div className="absolute top-20 right-3 w-64 max-h-[calc(100vh-120px)] bg-black/90 backdrop-blur-sm border border-teal-400/30 rounded-lg shadow-xl overflow-hidden pointer-events-auto">
      {/* Header */}
      <div className="px-4 py-3 border-b border-white/10">
        <h3 className="text-sm font-semibold text-teal-400 flex items-center gap-2">
          😀 Emoji Picker
        </h3>
      </div>

      <div className="p-4 space-y-4 overflow-y-auto max-h-[calc(100vh-200px)]">
        {Object.entries(EMOJI_CATEGORIES).map(([category, emojis]) => (
          <div key={category}>
            <h3 className="text-xs font-medium text-gray-300 uppercase tracking-wide mb-2">
              {category}
            </h3>
            <div className="grid grid-cols-6 gap-1">
              {emojis.map(emoji => (
                <button
                  className={`
                    h-10 w-10 text-xl rounded transition-all
                    hover:scale-110 hover:bg-teal-500/50
                    ${
                      currentEmoji === emoji
                        ? 'bg-teal-500 ring-2 ring-teal-400 ring-offset-2 ring-offset-black/90 scale-105'
                        : 'hover:bg-white/10'
                    }
                  `}
                  key={emoji}
                  onClick={() => handleEmojiSelect(emoji)}
                >
                  {emoji}
                </button>
              ))}
            </div>
          </div>
        ))}
      </div>

      {/* Tip */}
      <div className="px-4 py-2 text-[10px] text-gray-500 italic border-t border-white/10">
        💡 Tip: Click any emoji to change it
      </div>
    </div>
  )
}
